namespace olitest { ////////////////////////////////////////////////////////////



//***** ПЕЧАТЬ ИНФОРМАЦИИ О НАЧАЛЕ ТЕСТИРОВАНИЯ ********************************
inline void PrintStartTests(const char* text)
{
  std::cout << std::endl << "Начало тестирования " << text << std::endl;
}
////////////////////////////////////////////////////////////////////////////////





//***** ПЕЧАТЬ ИМЕНИ ТЕСТИРУЕМОГО МЕТОДА ***************************************
inline void PrintFunctionName(const char* name)
{
  std::cout << "* " << name << std::endl;
}
////////////////////////////////////////////////////////////////////////////////





//***** ПЕЧАТЬ СООБЩЕНИЯ СТАНДАРТНОГО ИСКЛЮЧЕНИЯ *******************************
inline void PrintException(std::exception& e)
{
  std::cout << e.what() << std::endl;
  std::terminate();
}
////////////////////////////////////////////////////////////////////////////////





//***** ПЕЧАТЬ СООБЩЕНИЯ НЕОЖИДАННОГО ИСКЛЮЧЕНИЯ *******************************
inline void PrintUnexpectedException()
{
  std::cout << "Неожиданное исключение" << std::endl;
  std::terminate();
}
////////////////////////////////////////////////////////////////////////////////



} // namespace olitest /////////////////////////////////////////////////////////

